package com.example.EmployeeLeaveManagement.enums;

/**
 * Enum representing different types of leave available in the HRMS system.
 * <p>Used to categorize leave requests and track leave balances for employees.</p>
 */
public enum LeaveTypeEnum {

    /** Casual leave for personal reasons or short-term absence */
    Casual,

    /** Sick leave for medical or health-related absence */
    Sick,

    /** Birthday leave granted to employees around their birthday */
    Birthday
}
